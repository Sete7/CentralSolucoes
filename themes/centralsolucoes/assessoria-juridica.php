            </p>
            <p>
                <strong>EMPRESÁRIO INDIVIDUAL:</strong> que exerce em nome próprio, atividade empresarial. Trata-se de uma empresa que é titulada por uma só pessoa física, que integraliza bens próprios à exploração do negócio. Um empresário em nome individual atua sem separação jurídica entre os seus bens pessoais e os seus negócios, ou seja, não vigora o princípio da separação do patrimônio.              
            </p>
            <p>
               <strong>LTDA:</strong> A sociedade limitada estabelece normas com base no valor investido por cada sócio. O nome de cada uma das empresas desse modelo é acompanhado da sigla “Ltda”, que significa “limitada”. Esse é um dos tipos de empresa predominantes no Brasil, e sua base implica no contrato social.
            </p>
            <p>
               <strong>EIRELI:</strong> A Empresa Individual de Responsabilidade Limitada (Eireli) é uma categoria empresarial que permite a constituição de uma empresa com apenas um sócio: o próprio empresário. Essa modalidade foi criada em 2011 e surgiu com o propósito de acabar com a figura do sócio “fictício”, prática comum em empresas registradas como sociedade limitada, que antes só poderiam ser constituídas por, no mínimo, duas pessoas. A Eireli permite a separação entre o patrimônio empresarial e privado.
            </p>
            
