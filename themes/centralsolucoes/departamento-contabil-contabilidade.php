                <h1>Perguntas frequentes</h1>
                <p>Confira as perguntas frequentes sobre a área Contábil:</p>
                <h1>Qual a documentação inicial é indispensável para o início das atividades no departamento contábil?</h1>
                <p>Primeiramente, nos casos onde a empresa vier transferida de outra contabilidade, é necessário o envio do balancete referente ao último fechamento realizado para a devida apropriação dos saldos. Feita a implantação, e nos casos de empresas recém-constituídas, adotamos como padrão o envio da documentação:</p>
                <div class="list-dep">
                    <ul>
                        <li><span>1.</span> Extrato bancário consolidado (conta corrente e conta-investimentos);</li>
                        <li><span>2.</span> Movimento de caixa;</li>
                        <li><span>3.</span> Controle de contas a pagar e a receber;</li>
                        <li><span>4.</span> Notas fiscais de entradas, saídas e serviços;</li>
                        <li><span>5.</span> Contratos de empréstimos;</li>
                        <li><span>6.</span> Registro de Inventário;</li>
                        <li><span>7.</span>  Registro de Imobilizado (Listagem de bens físicos);</li>                
                    </ul>
