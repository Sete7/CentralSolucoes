                <p>*Ressaltamos que a documentação supracitada é genérica, podendo ser necessário o envio de outros documentos, de acordo com a especificidade do cliente.</p>

                <h1>Os pagamentos da empresa em conta corrente do sócio ou de terceiros também entram na contabilidade, ou somente as que forem pagas através de conta em nome da empresa?</h1>                
                <p>Em respeito ao princípio da entidade, a contabilidade não deve confundir o patrimônio das pessoas físicas e jurídicas em suas demonstrações. Em decorrência deste entendimento, nos casos em que pagamentos de obrigações da pessoa jurídica forem realizados por sócios ou terceiros, a contabilidade irá proceder a baixa da dívida, entretanto, gerando uma nova obrigação com o responsável pelo seu pagamento.</p>

                <h1>Quais documentos devem ser enviados à contabilidade no momento da aquisição de bens móveis e imóveis?</h1>
                <p>Para os bens móveis é necessário o envio da nota fiscal, acompanhada dos comprovantes de pagamento. Já no caso dos bens imóveis, é necessário o envio do registro do imóvel, acompanhado do contrato de compra e venda. Lembrando que, em ambos os casos, na hipótese de pagamento realizado por meio de financiamento ou empréstimo bancário, é indispensável o envio desse contrato.</p>

                <h1>Na venda de um bem imobilizado existe incidência de algum imposto?</h1>
                <p>De acordo com o valor da venda poderá haver tributação exclusiva sobre o ganho de capital, que deverá ser apurado mediante à incidência da alíquota de 15% (quinze porcento) sobre a diferença positiva entre o valor da alienação e o custo de aquisição do bem diminuído, quando for o caso, da depreciação acumulada.</p>

