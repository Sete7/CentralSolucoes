                <h1>Perguntas frequentes</h1>
                <p>Confira as perguntas frequentes sobre a área Fiscal:</p>
                <h1>Qual o prazo de cancelamento da NF-e?</h1>
                <p>O prazo para cancelamento da NF-e é de 24 horas, após sua emissão. Após decorrido o prazo estabelecido pela legislação a nota não poderá mais ser cancelada.</p>
                <h1>O que é substituição tributária?</h1>
                <p>É um regime no qual a responsabilidade pelo recolhimento do Imposto sobre Circulação de Mercadorias e Prestação de Serviços (ICMS) é atribuída ao contribuinte que não é o próprio gerador da ação de venda. É o caso, por exemplo, de um fabricante de bebidas que faz o recolhimento integral do tributo e, por consequência, desobriga a rede atacadista que dele compra e também os pequenos mercados que serão responsáveis pela venda final ao consumidor. Os dois últimos atores da cadeia não precisarão se preocupar com o cálculo do ICMS na compra e na venda dos referidos produtos.</p>
                <h1>Quando devo enviar as notas fiscais para o escritório?</h1>
                <p>Todas as notas fiscais de entradas e saídas devem ser encaminhadas ao escritório no primeiro dia útil de cada mês.</p>
                <h1>O que é CFOP?</h1>
                <p>CFOP é a sigla utilizada para CÓDIGO FISCAL DE OPERAÇÕES E PRESTAÇÕES. Trata-se de um código que é utilizado para identificar a natureza da circulação da mercadoria ou prestação de serviços. Por meio do CFOP define-se se haverá ou não recolhimento de impostos.</p>
                <h1>O MEI (microempreendedor individual) pode emitir nota fiscal?</h1>
                <p>Sim, ele pode solicitar na gráfica a autorização do bloco manual de nota fiscal, ou ainda pode se dirigir à Secretaria de Fazenda do Distrito Federal e solicitar autorização para emitir a Nota Fiscal Eletrônica Avulsa.</p>
                <h1>É difícil emitir o novo modelo de nota fiscal NFCe?</h1>
                <p>Não. O empresário terá que adquirir um certificado digital e um programa emissor de nota fiscal eletrônica em empresas especializadas na área de automação, e terá um custo que varia de acordo com a necessidade do cliente.</p>

